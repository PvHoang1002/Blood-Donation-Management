    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1 && (Boolean) jTable1.getValueAt(selectedRow, 0) == false) {
            isChoosen = false;
        } else {
            isChoosen = true;
            storedEventName = jTable1.getModel().getValueAt(selectedRow, 1).toString();
            storedDate = jTable1.getModel().getValueAt(selectedRow, 2).toString();
            storedNoParticipant = jTable1.getModel().getValueAt(selectedRow, 6).toString();
            
            try {
                Connection con = ConnectionProvider.getConnection();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select * from event join doctor on event.HeadDoctor = doctor.DoctorID where StartDate >= curdate() order by StartDate asc;");
                while (rs.next()) {
                    if (storedEventName.equals(rs.getString("Name"))
                            && storedDate.equals(new SimpleDateFormat("dd-MM-yyyy").format(rs.getDate("StartDate")))) {
                        storedHeadDoctor = rs.getString("DoctorID");
                        break;
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

