    private void setSelectionEnableOrDisable(JTable table) {
        table.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent event) {
                int selectedRow = table.getSelectedRow();
                if ((Boolean) table.getValueAt(selectedRow, 0)) {
                    for (int i = 0; i < table.getRowCount(); i++) {
                        if (i != selectedRow) {
                            table.setValueAt(false, i, 0);
                        }
                    }
                }
            }
        });
    }

    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try {
            Connection con = ConnectionProvider.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from event join doctor on event.HeadDoctor = doctor.DoctorID where StartDate >= curdate() order by StartDate asc;");
            while (rs.next()) {
                String name = rs.getString("Name");
                SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
                String startDate = dateFormat.format(rs.getDate("StartDate"));
                String endDate = dateFormat.format(rs.getDate("EndDate"));
                String location = rs.getString("Location");
                String headDoctor = rs.getString("Fullname");
                String participants = rs.getString("NoParticipants");
                
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.addRow(new Object[]{false, name, startDate, endDate, location, headDoctor, participants});
            }
            jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
            setSelectionEnableOrDisable(jTable1);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_formComponentShown

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nextDonationDate = null;
        try {
            Connection con = ConnectionProvider.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from donor");
            while (rs.next()) {
                if ((UserHome.getEmail()).equals(rs.getString("Email"))) {
                    nextDonationDate = rs.getString("NextDonationDate");
                    break;
                }
            }
            if (nextDonationDate == null) {
                nextDonationDate = "00-00-0000";
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        if (isChoosen == false) {
            JOptionPane.showMessageDialog(null, "No event has been selected");
        } else if (!UserRegisteredEvent.isEmpty()) {
            JOptionPane.showMessageDialog(null, "You have already registered one event");
        } else try {
            if (!(new SimpleDateFormat("dd-MM-yyyy").parse(storedDate)).after((new SimpleDateFormat("dd-MM-yyyy").parse(nextDonationDate)))) {
                JOptionPane.showMessageDialog(null, "You are not able to donate until " + nextDonationDate);
            } else {
                int participants = Integer.parseInt(storedNoParticipant) + 1;
                try {
                    Connection con = ConnectionProvider.getConnection();
                    Statement st = con.createStatement();
                    st.executeUpdate("update event set NoParticipants = '" + String.valueOf(participants) + "' where Name = '" + storedEventName + "'");
                    ResultSet rs = st.executeQuery("select * from donor");
                    while (rs.next()) {
                        if (UserHome.getEmail().equals(rs.getString("Email"))) {
                            String donorID = rs.getString("DonorID");
                            st.executeUpdate("insert into registration value ('" + donorID + "', '" + storedEventName + "', str_to_date('" + storedDate + "', \"%d-%m-%Y\"),  '" + storedHeadDoctor + "')");
                            break;
                        }
                    }
                    UserHome.hasRegistered = true;
                    JOptionPane.showMessageDialog(null, "Registered successfully");
                    setVisible(false);
                    new UserRegisterEvent().setVisible(true);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        } catch (ParseException ex) {
            Logger.getLogger(UserRegisterEvent.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

