    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!isChoosen) {
            JOptionPane.showMessageDialog(null, "There is no event to remove");
        } else {
            int confirmation = JOptionPane.showConfirmDialog(null, "Removing these events?", "Select", JOptionPane.YES_NO_OPTION);
            if (confirmation == 0) {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                for (int i = 0; i < jTable1.getRowCount(); i++) {
                    if ((Boolean) jTable1.getValueAt(i, 0)) {
                        System.out.println(jTable1.getValueAt(i, 1).toString());
                        try {
                            String eventName = jTable1.getValueAt(i, 1).toString();
                            String startDate = jTable1.getValueAt(i, 2).toString();
                            String endDate = jTable1.getValueAt(i, 3).toString();
                            String location = jTable1.getValueAt(i, 4).toString();

                            Connection con = ConnectionProvider.getConnection();
                            Statement st = con.createStatement();
                            st.executeUpdate("delete from registration where EventName = '" + eventName + "' and DonationDate = str_to_date('" + startDate + "', \"%d-%m-%Y\")");
                            st.executeUpdate("delete from event where Name = '" + eventName + "' and StartDate = str_to_date('" + startDate + "', \"%d-%m-%Y\") and EndDate = str_to_date('" + endDate + "', \"%d-%m-%Y\") and Location = '" + location + "'");                           
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, e);
                        }
                    }
                }
                JOptionPane.showMessageDialog(null, "Removed successfully");
                setVisible(false);
                new AdminRemoveEvent().setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

